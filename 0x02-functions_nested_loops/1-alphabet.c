#include "holberton.h"
/**
 * main - Entry point
 * Description : Prints alphabet in lowercase using _putchar
 * Return: Always 0 (Success)
*
*/
void print_alphabet(void)
{
char alpha[] = "abcdefghijklmnopqrstuvwxyz";
int i;
for (i = 0 ; i < alpha[i] ; i++)
{
_putchar(alpha[i]);
}
_putchar('\n');
}
